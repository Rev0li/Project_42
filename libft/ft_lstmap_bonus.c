#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;

	while (lst != NULL)
	{
		f(lst->content);
		new_lst = lst;
		lst = lst->next;
	}
	return (new_lst);
}
